
// a basic adding function
const addFunc = function(num1, num2) {
    return num1 + num2
}

// a basic subtraction function
const subFunc = function(num1, num2) {
    return num1 - num2
}

// a basic multiplication function
const multFunc = function(num1, num2) {
    return num1 * num2
}

// a basic division function
const divFunc = function(num1, num2) {
    return num1 - num2
}